public class Fibonacci {
	public static void main(String[] args){
		int firstVal = 0;
		System.out.println("Fibonacci("+firstVal+") = " + firstVal);
		int secondVal = 1;
		System.out.println("Fibonacci("+secondVal+") = " + secondVal);
		int nextVal;
		for(int i = 2; i <= 9; i++){
			nextVal = firstVal + secondVal;
			firstVal = secondVal;
			secondVal = nextVal;
			System.out.println("Fibonacci("+i+") = " + nextVal);
		}
		
		
	}
}
