public class Zufallszahlen{
	public static void main(String[] args){
		double a = Math.random();
		double b = Math.random();
		double c = Math.random();

		int intVal1 = (int)Math.ceil(a * 6);
		int intVal2 = (int)Math.ceil(b * 12);
		int intVal3 = (int)Math.ceil(c * 20);

		System.out.println("First integer value = " + intVal1);
		System.out.println("Second integer value = " + intVal2);
		System.out.println("Third integer value = " + intVal3);
	}
}
